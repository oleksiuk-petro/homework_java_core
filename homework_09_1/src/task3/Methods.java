//в цьому класі описуємо арифметичні методи
package task3;
public class Methods {

    //метод додавання двох чисел
    void addition(double var1, double var2){
        System.out.println("a + b = " + (var1 + var2));
    }

    //метод віднімання двох чисел
    void subtraction(double var1, double var2){
        System.out.println("a - b = " + (var1 - var2));
    }

    //метод множення двох чисел
    void multiplication(double var1, double var2){
        System.out.println("a * b = " + (var1 * var2));
    }

    //метод ділення двох чисел
    void division(double var1, double var2){
        System.out.println("a / b = " + (var1 / var2));
    }
}
